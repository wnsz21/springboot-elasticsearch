    public static List<Goods> parseJd(String keyword) throws IOException {
        String url = "http://search.jd.com/Search?keyword=" + keyword;
        List<Goods> goodsList = new ArrayList<>();


        Map<String, String> data = new HashMap<>();
        //data.put("waybillNo", "DD1838768852");

        Connection connect = Jsoup.connect(url)
                .timeout(30000);
                //.data(data)
                //.header("User-Agent", getUserAgent());

        Document document = connect.get();

        Element j_goodsList = document.getElementById("J_goodsList");

        Elements lis = j_goodsList.getElementsByTag("li");

        lis.forEach(li -> {
            Goods goods = new Goods();
            String name = li.getElementsByClass("p-name").eq(0).text();
            String price = li.getElementsByClass("p-price").eq(0).text();
            String img = li.getElementsByTag("img").eq(0).attr("data-lazy-img");
            goodsList.add(Goods.builder().name(name).price(price).img(img).build());
        });

        return goodsList;
    }

